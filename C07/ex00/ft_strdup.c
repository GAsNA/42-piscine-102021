#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		len_src;
	char	*dest;

	len_src = 0;
	while (src[len_src])
		len_src++;
	dest = malloc((len_src + 1) * sizeof(char));
	if (!dest)
		return (0);
	i = -1;
	while (src[++i])
		dest[i] = src[i];
	dest[i] = '\0';
	return (dest);
}
